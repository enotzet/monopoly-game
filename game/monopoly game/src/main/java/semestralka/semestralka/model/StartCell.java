package semestralka.semestralka.model;

/**
 * Represents the "Start" cell in the Monopoly game.
 * Extends the Cell class.
 */
public class StartCell extends Cell {

    /**
     * Constructor for the StartCell class.
     * Initializes the name_of_cell property to "Start".
     */
    public StartCell() {
        name_of_cell = "Start";
    }
}
