package semestralka.semestralka.model;

/**
 * Represents the "Go to Jail" cell in the Monopoly game.
 * Extends the Cell class.
 */
public class GoToJail extends Cell {

    /**
     * Constructor for the GoToJail class.
     * Initializes the name_of_cell property to "Go to the Jail!".
     */
    public GoToJail() {
        name_of_cell = "Go to the Jail!";
    }
}
